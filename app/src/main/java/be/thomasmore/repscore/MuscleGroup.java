package be.thomasmore.repscore;

public class MuscleGroup {
    private long id;
    private String name;

    public MuscleGroup() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
